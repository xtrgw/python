#定义一个椭圆
def  ellipse(m,n,a):
    t.goto(m,n)
    t.setheading(0)
    t.pd()
    t.begin_fill()
    b=a/5
    for i in range(120):
        if 0<=i<30 or 60<=i<90:
            a=a+b
            t.lt(3)
            t.fd(a)
        else:
            a=a-b
            t.lt(3)
            t.fd(a)
            t.end_fill()
            t.pu()
        return
    #初始设置
